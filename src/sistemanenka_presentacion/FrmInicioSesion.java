    private void btnContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContinuarActionPerformed
        if (!txtUsuario.getText().isEmpty() && !txtContrasenia.getText().isEmpty()) {
            
            Empleado empleado = negocios.iniciarSesion(txtUsuario.getText(), txtContrasenia.getText());
            
            if(empleado != null){
                FmRealizarVenta realizarVaenta = new FmRealizarVenta(empleado);
                realizarVaenta.setVisible(true);
                dispose();
            } else {
                limpiarCampos();
                JOptionPane.showMessageDialog(this, "El usuario o contraseña son incorrectas.", "Credenciales incorrectas", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debes llenar todos los campos.", "Campos vacios", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnContinuarActionPerformed

    private void limpiarCampos(){
        this.txtUsuario.setText("");
        this.txtContrasenia.setText("");
    }
    
    private void inicializar(){
        this.setTitle("Dulcería La Abue | Iniciar Sesión");
        this.setLocationRelativeTo(null);
        this.negocios = getFachadaNegocios();
    }

